
# int
x = int(2)
y = int(2.8)
z = int('2')

print(x, y, z)
print('A Variável x é do tipo ', (type(x)))
print('A Variável y é do tipo ', (type(y)))
print('A Variável z é do tipo ', (type(z)))

# No exemplo acima por ter colocado o int antes do valor declarado, todos os valores foram alterados para o tipo int.


#float
a = float(2.3)
b = float(2)
c = float("2.3")
d = float("2")

print(a, b, c, d)
print("A Variável a é do tipo ", type(a))
print("A Variável b é do tipo ", type(b))
print("A Variável c é do tipo ", type(c))
print("A Variável d é do tipo ", type(d))

# No exemplo acima por ter colocado o float antes do valor declarado, todos os valores foram alterados para o tipo float.


