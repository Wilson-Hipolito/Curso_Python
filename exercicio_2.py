#Exercício n° 2 - Área de um Quadrado.
#Formular para resolver o exercício: area = comprimento * altura

print("Exercício 2° - Calculo da área de um Quadrado.")

base = input("Qual o tamanho da base do seu quadrado? ")
altura = input("Qual o tamanho da altura do seu quadrado? ")
area = float(base) * float(altura)

print(f"O seu quadrado possui area de: {area} Unidades de medida")
